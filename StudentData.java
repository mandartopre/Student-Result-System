    private void jBtnStudentResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnStudentResultActionPerformed
         
        double[] R=new double[14];
        R[0]=Double.parseDouble(jTxtMath.getText());
        R[1]=Double.parseDouble(jTxtEnglish.getText());
        R[2]=Double.parseDouble(jTxtBiology.getText());
        R[3]=Double.parseDouble(jTxtComputing.getText());
        R[4]=Double.parseDouble(jTxtChemistry.getText());
        R[5]=Double.parseDouble(jTxtPhysics.getText());
        R[6]=Double.parseDouble(jTxtSocialStudies.getText());
        R[7]=Double.parseDouble(jTxtBusiness.getText());
        
        //For totalscore and average
        
        R[8]=(R[0]+R[1]+R[2]+R[3]+R[4]+R[5]+R[6]+R[7]/8);  //Average
        R[9]=R[0]+R[1]+R[2]+R[3]+R[4]+R[5]+R[6]+R[7];     //Total
        
        String Average=String.format("%.0f",R[8]);
        jTxtAverage.setText(Average);
        
        String TotalScore=String.format("%.0f",R[9]);
        jTxtTotalScore.setText(TotalScore);
        
        
        
        if(R[9]>=700)
        {
            jTxtRanking.setText("A");
        }
        else if (R[9]>=600)
        {
            jTxtRanking.setText("B");
        }
         else if (R[9]>=500)
        {
            jTxtRanking.setText("C");
        }
         else if (R[9]>=400)
        {
            jTxtRanking.setText("D");
        }
         else if (R[9]>=300)
        {
            jTxtRanking.setText("E");
        }
         else if (R[9]>=200)
        {
            jTxtRanking.setText("Fail");
        }
        
        //---------------------------Database------------------------
        String sql="INSERT INTO Student(StudentID, Term,Math,English,Biology,Computing,Chemistry,Physics,SocialStudies,Business,TotalScore,Average,Ranking)VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?)";

        try
        {
            pst= conn.prepareStatement(sql);
            pst.setString(1,jTxtStudentID.getText());
            pst.setString(2,(String)jComboBox.getSelectedItem());
            pst.setString(3,jTxtMath.getText());
            pst.setString(4,jTxtEnglish.getText());
            pst.setString(5,jTxtBiology.getText());
            pst.setString(6,jTxtComputing.getText());
            pst.setString(7,jTxtChemistry.getText());
            pst.setString(8,jTxtPhysics.getText());
            pst.setString(9,jTxtSocialStudies.getText());
            pst.setString(10,jTxtBusiness.getText());
            pst.setString(11,jTxtTotalScore.getText());
            pst.setString(12,jTxtAverage.getText());
            pst.setString(13,jTxtRanking.getText());
           

            pst.execute();
            JOptionPane.showMessageDialog(null,"Data Added into system succesfully");
            rs.close();
            pst.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        updateTable();
    }//GEN-LAST:event_jBtnStudentResultActionPerformed
    public void updateTable()
    {
            conn=StudentData.ConnectDb();
            if(conn!=null)
            {    
                String sql="Select StudentID, Term,Math,English,Biology,Computing,Chemistry,Physics,SocialStudies,Business,TotalScore,Average,Ranking from Student";
                try
                {
                    pst=conn.prepareStatement(sql);
                    rs= pst.executeQuery();
                    Object[] columnData=new Object[13];
                    while (rs.next())
                            {
                                columnData[0]=rs.getString("StudentID");
                                columnData[1]=rs.getString("Term");
                                columnData[2]=rs.getString("Math");
                                columnData[3]=rs.getString("English");
                                columnData[4]=rs.getString("Biology");
                                columnData[5]=rs.getString("Computing");
                                columnData[6]=rs.getString("Chemistry");
                                 columnData[7]=rs.getString("Physics");
                                columnData[8]=rs.getString("SocialStudies");
                                columnData[9]=rs.getString("Business");
                                columnData[10]=rs.getString("TotalScore");
                                columnData[11]=rs.getString("Average");
                                columnData[12]=rs.getString("Ranking");
                                model.addRow(columnData);
                            }
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null,e);
                }
            }
        
        
    }
